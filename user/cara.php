<h3 class="card-title text-center mb-5 fw-light fs-5">Cara Menjadi Investor</h3>
<p>Untuk Menjadi investor melalui online, Anda dapat melakukan langkah-langkah sebagai berikut:</p>
<li>1. Hal pertama sebelum menjadi investor, yaitu anda diharuskan sudah melihat paket investasi yang ditawarkan oleh Hydromart di menu <a href="layanan.php">Layanan.</a> </li>
<li>2. Hal kedua sebelum melakukan pemesanan, anda diharuskan sudah terdaftar sebagai member. Jika belum, maka terlebih dahulu melakukan pendaftaran.</li>
<li>3. Isi data diri pribadi sesuai dengan form yang disediakan, kemudian pilih paket investasi.</li>
<li>4. Setelah berhasil mendaftarkan diri anda, maka halaman akan berpindah ke halaman login. Isi data diri anda sesuai dengan form yang disediakan.</li>
<li>5. Setelah berhasil login, anda akan masuk ke dalam halaman pesanan, anda dapat melihat data pribadi anda dan paket yang dipilih, serta melakukan konfirmasi pembayaran.</li>
<li>6. Di halaman konfirmasi pembayaran, isi data diri, dan upload foto bukti transaksi ke form yang sudah disediakan.</li>
<li>7. Setelah anda berhasil melakukan transaksi, anda akan di arahkan ke halaman unduhan.</li>
<li>8. Di halaman unduhan, anda di wajibkan untuk mendownload form yang sudah disediakan.</li>
<li>9. Setelah sudah melakukan semua step-step di atas, mohon tunggu konfirmasi dari admin.</li>

